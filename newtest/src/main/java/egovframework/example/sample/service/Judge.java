package egovframework.example.sample.service;

public class Judge {
	private int judge_id;
	private String judge_name;
	private String phone_email;
	public Judge() {
	}
	public int getJudge_id() {
		return judge_id;
	}
	public void setJudge_id(int judge_id) {
		this.judge_id = judge_id;
	}
	public String getJudge_name() {
		return judge_name;
	}
	public void setJudge_name(String judge_name) {
		this.judge_name = judge_name;
	}
	public String getPhone_email() {
		return phone_email;
	}
	public void setPhone_email(String phone_email) {
		this.phone_email = phone_email;
	}
	
}
