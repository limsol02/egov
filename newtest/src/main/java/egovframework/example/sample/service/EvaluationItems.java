package egovframework.example.sample.service;

public class EvaluationItems {
	private int evaluation_id;
	private String evaluation_items;
	
	public EvaluationItems() {}
	
	public int getEvaluation_id() {
		return evaluation_id;
	}
	public void setEvaluation_id(int evaluation_id) {
		this.evaluation_id = evaluation_id;
	}
	public String getEvaluation_items() {
		return evaluation_items;
	}
	public void setEvaluation_items(String evaluation_items) {
		this.evaluation_items = evaluation_items;
	}
}
